public class Apple {
    private String color ;
    private int width ;

    public Apple(String color, int width){
        this.color = color ;
        this.width = width ;

    }

    public String getColor(){
        return color ;
    }

    public int getWidth(){
        return width ;
    }

    public void setColor( String color){
        this.color = color ;
    }

    public void setWidth( int width){
        this.width = width ;
    }
}
